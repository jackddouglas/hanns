export default function Register() {
  const { writeContract } = useWriteContract();
  const { data: walletClient } = useWalletClient();
  const [name, setName] = useState('');
  const [description, setDescription] = useState('');
  const [file, setFile] = useState('');
  const [cid, setCid] = useState('');
  const [minting, setMinting] = useState(false);

  const inputFile = useRef<HTMLInputElement>(null);

  const uploadFile = async (fileToUpload: any) => {
    try {
      setMinting(true);
      const data = new FormData();
      data.set("file", fileToUpload);
      const res = await fetch("/api/files", {
        method: "POST",
        body: data,
      });
      const resData = await res.json();
      setCid(resData.IpfsHash);
      setMinting(false);
      return `https://${process.env.NEXT_PUBLIC_GATEWAY_URL}/ipfs/${resData.IpfsHash}`;
    } catch (e) {
      console.log(e);
      setMinting(false);
      alert("Trouble uploading file");
    }
  };

  const handleChange = (e: any) => {
    setFile(e.target.files[0]);

    try {
      const imageUrl = uploadFile(e.target.files[0]);
      const metadata = {
        name,
        description,
        image: imageUrl,
      };
      const metadataUrl = uploadFile(metadata);

      safeMint(walletClient?.account, metadataUrl);

      setName('');
      setDescription('');
    } catch (e) {
      console.error('Error minting NFT:', e);
    }
  };

  function safeMint(to: any, uri: any) {
    writeContract({
      address,
      abi,
      functionName: "safeMint",
      args: [to, uri],
    })
  }

  return (
    <main className="flex min-h-screen flex-col items-center justify-between p-24">
      <div className="z-10 max-w-5xl w-full items-center justify-between font-mono text-sm lg:flex">
        <ConnectBtn />
      </div>

      <input type="file" id="file" ref={inputFile} onChange={handleChange} />
      <Button disabled={minting} onClick={() => inputFile.current?.click()}>
        {minting ? "Uploading..." : "Upload"}
      </Button>
      {cid && (
        <img
          src={`https://${process.env.NEXT_PUBLIC_GATEWAY_URL}/ipfs/${cid}`}
          alt="Image from IPFS"
        />
      )}
    </main>
  );
}
